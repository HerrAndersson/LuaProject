function keyHandler(code)
	local var = code

	if(var == 3) then
		move(5, 0)
	elseif(var == 0) then
		move(-5, 0) 
	elseif(var == 22) then
		move(0, -5)
	elseif(var == 18) then
		move(0, 5)
	elseif(var == 11) then
		local int = "1"
		loadLevel(int)
	else 
		move(0,0)
	end

	return var
end

function move(x, y)
	moveC(x, y)
	return 1
end

function render()

	clearWindowC();

	for i=1, worldHeight, 1
	do
		for j=1, worldWidth, 1
		do
			drawSquareC((j-1) * dimensions, (i-1) * dimensions, dimensions, world[i][j]);
		end
	end
	
	drawPlayerC()
		
	displayWindowC();

	return 1;
end

function loadLevel(int)
	file = io.type(int)
	print(file)
	if file == nil then 
		clearLvl = assert(loadfile("clearLvl.lua"))
		clearLvl(int)
	end
end
	